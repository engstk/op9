static int syna_set_large_thd(void *chip_data, int large_thd)
{
	struct syna_tcm_data *tcm_info = (struct syna_tcm_data *)chip_data;
	int ret = 0;
	unsigned short value;
	uint16_t g_dark_zone_enable = 0;
	uint16_t g_grip_enabled = 0;
	u16 config = 0;

	if (!tcm_info || *tcm_info->in_suspend) {
		return -1;
	}

	TPD_INFO("%s:large_thd %d.\n", __func__, large_thd);

	value = large_thd & 0xF0;
	CLR_BIT(g_grip_enabled, 0xF0);
	SET_BIT(g_grip_enabled, value);

	value = (large_thd & 0x0F) << 8;
	CLR_BIT(g_dark_zone_enable, 0xF00);
	SET_BIT(g_dark_zone_enable, value);

	ret = syna_tcm_set_dynamic_config(tcm_info, DC_GRIP_ENABLED, 0x01 | g_grip_enabled);
	if (ret < 0) {
		TPD_INFO("%s:failed to set DC_GRIP_ENABLED\n", __func__);
		return ret;
	}
	ret = syna_tcm_get_dynamic_config(tcm_info, DC_GRIP_ENABLED, &config);
	TPD_INFO("DC_GRIP_ENABLE is 0x%X\n", config);
	if (g_tp->limit_switch == 1)
		ret = syna_tcm_set_dynamic_config(tcm_info, DC_DARK_ZONE_ENABLE, 0x03 | g_dark_zone_enable);
	else if (g_tp->limit_switch == 3)
		ret = syna_tcm_set_dynamic_config(tcm_info, DC_DARK_ZONE_ENABLE, 0x0C | g_dark_zone_enable);
	else
		ret = syna_tcm_set_dynamic_config(tcm_info, DC_DARK_ZONE_ENABLE, 0x05 | g_dark_zone_enable);
	if (ret < 0) {
		TPD_INFO("%s:failed to set DC_DARK_ZONE_ENABLE\n", __func__);
		return ret;
	}
	ret = syna_tcm_get_dynamic_config(tcm_info, DC_DARK_ZONE_ENABLE, &config);
	TPD_INFO("DC_DARK_ZONE_ENABLE is 0x%X\n", config);

	return ret;
}

