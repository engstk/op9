static int syna_tcm_refresh_switch(void *chip_data, int fps)
{
	int retval = -1;
	unsigned short send_value = 1;
	struct syna_tcm_data *chip_info = (struct syna_tcm_data *)chip_data;

	if (chip_info == NULL) {
		return -1;
	}
	chip_info->display_refresh_rate = fps;

	if (!*chip_info->in_suspend) {
		switch (fps) {
		case 60:
			send_value = 1;
			break;
		case 90:
			send_value = 3;
			break;
		case 120:
			send_value = 2;
			break;
		default:
			return 0;
		}
		retval = syna_tcm_set_dynamic_config(chip_info, DC_SET_REPORT_FRE, send_value);
		if (retval < 0) {
			TPD_INFO("Failed to set dynamic report frequence config\n");
		}
	} else
		TPD_INFO("no need to switch reprot rate\n");

	TPD_INFO("%s: refresh_switch: %d HZ %s!\n", __func__, fps, retval < 0 ? "failed" : "success");

	return retval;
}

